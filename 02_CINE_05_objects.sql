-- 8)  VUE dictionnare_V
---------------------------------------------------------
CREATE OR REPLACE VIEW cine.dictionnaire_v AS
SELECT
    atc.table_name,atc.column_name, atc.data_type, atc.data_length, atc.data_precision,atc.data_scale,atc.nullable, atc.data_default,acc.comments AS commentaires_colonne,
    ac.constraint_type, ac.constraint_name
FROM all_tab_columns atc
LEFT JOIN all_col_comments acc
       ON atc.owner = acc.owner
      AND atc.table_name = acc.table_name
      AND atc.column_name = acc.column_name
LEFT JOIN all_cons_columns acon
       ON atc.owner = acon.owner
      AND atc.table_name = acon.table_name
      AND atc.column_name = acon.column_name
LEFT JOIN all_constraints ac
       ON acon.owner = ac.owner
      AND acon.constraint_name = ac.constraint_name
WHERE atc.owner = 'CINE'
  AND atc.table_name IN ('CINEMAS', 'GENRES', 'FILMS', 'SALLES', 'SEANCES', 'CLIENTS', 'RESERVATIONS')
ORDER BY atc.table_name, atc.column_id;